public class AddNumbers {
    public static void main(String[] args) {
        int a = 20;
        int b = 30;

        int sum = a + b;
        int product = a * b;
        int difference = a - b;

        System.out.println("Sum: " + sum);
        System.out.println("Product: " + product);
        System.out.println("Difference: " + difference);
    }
}
